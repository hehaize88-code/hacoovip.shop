import { mkdirSync, readdirSync, rmSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceRoot = path.join(root, "public", "products");
const outputRoot = path.join(root, "public", "optimized", "products");
const manifestPath = path.join(root, "lib", "imageManifest.js");
const supportedExtensions = new Set([".gif", ".jpeg", ".jpg", ".png", ".webp"]);

function walk(directory, files = []) {
  for (const entry of readdirSync(directory)) {
    const absolute = path.join(directory, entry);
    if (statSync(absolute).isDirectory()) walk(absolute, files);
    else if (supportedExtensions.has(path.extname(entry).toLowerCase())) files.push(absolute);
  }
  return files;
}

function outputWidths(relativePath, sourceWidth) {
  const requested = relativePath === "shoes-60.jpg"
    ? [480, 960]
    : relativePath.startsWith("catalog/")
      ? [360, 720]
      : [480, 750];
  return [...new Set(requested.map((width) => Math.min(width, sourceWidth)))].sort((a, b) => a - b);
}

rmSync(outputRoot, { recursive: true, force: true });
mkdirSync(outputRoot, { recursive: true });

const manifest = {};
for (const source of walk(sourceRoot).sort()) {
  const relativePath = path.relative(sourceRoot, source).split(path.sep).join("/");
  const publicPath = `/products/${relativePath}`;
  const metadata = await sharp(source, { animated: true }).metadata();
  const widths = outputWidths(relativePath, metadata.width);
  const stem = relativePath.slice(0, -path.extname(relativePath).length);
  const entry = {
    width: metadata.width,
    height: metadata.pageHeight || metadata.height,
    avif: [],
    webp: [],
  };

  for (const width of widths) {
    const outputStem = path.join(outputRoot, `${stem}-${width}`);
    mkdirSync(path.dirname(outputStem), { recursive: true });

    await sharp(source, { animated: true })
      .resize({ width, withoutEnlargement: true })
      .webp({ quality: 74, effort: 4 })
      .toFile(`${outputStem}.webp`);
    entry.webp.push({ src: `/optimized/products/${stem}-${width}.webp`, width });

    if ((metadata.pages || 1) === 1) {
      await sharp(source)
        .resize({ width, withoutEnlargement: true })
        .avif({ quality: 52, effort: 3 })
        .toFile(`${outputStem}.avif`);
      entry.avif.push({ src: `/optimized/products/${stem}-${width}.avif`, width });
    }
  }

  manifest[publicPath] = entry;
}

writeFileSync(
  manifestPath,
  `// Generated by scripts/generate-responsive-images.mjs.\nexport const imageManifest = ${JSON.stringify(manifest, null, 2)};\n`,
);

console.log(`Generated responsive AVIF/WebP variants for ${Object.keys(manifest).length} source images.`);
